#반복문

for i in range(10):
    print("방문을 환영합니다!")


    

#끝에 : 있어야 하고, 들여쓰기 써야함
# i가 1부터 5까지 변경되며 반복되는 구조
    
for i in[1,2,3,4,5,6,7,8]:
    print("방문을 환영합니다.")
    print(i)
    print('%s'%i) ## 이렇게 출력하는 것도 가능함


